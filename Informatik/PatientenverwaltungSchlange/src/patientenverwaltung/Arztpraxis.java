    private void RemovePatient(int index){
        int counter = 0;
        Queue<Patient> temp = new Queue<>();
        while(patienten.front() != null){
            
            if(counter == index){
                patienten.dequeue();
            }
            else{
                temp.enqueue(patienten.front());
                patienten.dequeue();
            }
            counter++;
        }
        patienten = temp;
    }
    
    private Patient AtIndex(int index){
        Patient finnal = null;
        Patient temp = patienten.front();
        Queue<Patient> temmp = new Queue<>();
        int counter = 0;
        while(temp != null){
            if(counter == index){
                finnal = temp;
            }
            temmp.enqueue(temp);
            patienten.dequeue();
            temp = patienten.front();
            counter++;
        }
        while(temmp.front() != null){
            patienten.enqueue(temmp.front());
            temmp.dequeue();
        }
        return finnal;
    }
    
    private int IndexOf(Patient meinPatient){
        int counter = 0;
        boolean addToCounter =true;
        Patient temp = patienten.front();
        Queue<Patient> tempQueue = new Queue<>();
        while(temp != null){
            if(temp.equals(meinPatient)){
                addToCounter = false;
            }
            if(addToCounter){
                counter++;
            }
            tempQueue.enqueue(temp);
            patienten.dequeue();
            temp = patienten.front();
        }
        patienten = tempQueue;
        return counter;
    }
    
    private int GetLength(){
        Queue<Patient> temmp = new Queue<>();
        int counter = 0;
        while(patienten.front() != null){
            temmp.enqueue(patienten.front());
            patienten.dequeue();
            counter++;
        }
        while(temmp.front() != null){
            patienten.enqueue(temmp.front());
            temmp.dequeue();
        }
        return counter;
    }
    
