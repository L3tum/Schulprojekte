class ThreadDemo extends Thread {
   private Thread t;
   private final String threadName;
   private final JProgressBar jpb1;
   private final JProgressBar jpb2;
   
   ThreadDemo(String name, JProgressBar jpb1, JProgressBar jpb2){
       this.jpb1 = jpb1;
       this.jpb2 = jpb2;
       threadName = name;
       System.out.println("Creating " +  threadName );
   }
   @Override
   public void run() {
      while(true){
          jpb1.setOrientation(Orientation.VERTICAL.ordinal());
          jpb2.setOrientation(Orientation.HORIZONTAL.ordinal());
          //jpb1.setVisible(!jpb1.isVisible());
          //jpb2.setVisible(!jpb2.isVisible());
          try{
              Thread.sleep(100);
          } catch(Exception ex){
              
          }
          jpb1.setOrientation(Orientation.HORIZONTAL.ordinal());
          jpb2.setOrientation(Orientation.VERTICAL.ordinal());
          //jpb1.setVisible(!jpb1.isVisible());
          //jpb2.setVisible(!jpb2.isVisible());
          try{
              Thread.sleep(100);
          } catch(Exception ex){
              
          }
      }
   }
   
   @Override
   public void start ()
   {
      System.out.println("Starting " +  threadName );
      if (t == null)
      {
         t = new Thread (this, threadName);
         t.start ();
      }
   }

}


