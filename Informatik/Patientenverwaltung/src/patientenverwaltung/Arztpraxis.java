    private void ValueChangedInJList1(){
        if(jList1.getSelectedIndex() != -1){
            DefaultListModel newListModel = new DefaultListModel();
            int sel = jList1.getSelectedIndex();
            this.jTfBg.setText(patienten[sel].getBlutgruppe());
            this.jTfNn.setText(patienten[sel].getNotfallnummer());
            this.jTfHnEdit.setText(patienten[sel].getHausnummer());
            this.jTfOEdit.setText(patienten[sel].getOrt());
            this.jTfPlEdit.setText(patienten[sel].getPlz());
            this.jTfRnEdit.setText(patienten[sel].getRufnummer());
            this.jTfStEdit.setText(patienten[sel].getStrasse());
            this.jTfN.setText(patienten[sel].getName());
            this.jTfVn.setText(patienten[sel].getVorname());
            for(String allergie : patienten[sel].getAllergien()){
                if(allergie != null && !allergie.equals(""))
                    newListModel.addElement(allergie);
            }
            jListAllerg.setModel(newListModel);
        }
        else{
            this.jTfBg.setText("");
            this.jTfNn.setText("");
            this.jTfHnEdit.setText("");
            this.jTfOEdit.setText("");
            this.jTfPlEdit.setText("");
            this.jTfRnEdit.setText("");
            this.jTfStEdit.setText("");
            this.jTfVn.setText("");
            this.jTfN.setText("");
            jListAllerg.setModel(new DefaultListModel());
        }
    }
    
    private int getEmptyEntry(){
        for(int i = 0; i < patienten.length; i++){
            if(patienten[i] == null){
                return i;
            }
        }
        return -1;
    }
    
    private int getEmptyEntry(String[] array){
        for(int i = 0; i < array.length; i++){
            if(array[i] == null || "".equals(array[i])){
                return i;
            }
        }
        return -1;
    }
    
