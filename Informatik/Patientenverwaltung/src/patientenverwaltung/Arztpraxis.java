    private void jListAllergMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListAllergMouseClicked
        if(evt.getButton() == MouseEvent.BUTTON3){
            int something = jList1.getSelectedIndex();
            int somethingelse = jListAllerg.getSelectedIndex();
            jListAllerg.setSelectedIndex(-1);
            patienten[something].getAllergien()[somethingelse] = "";
            for(int i = somethingelse; i < patienten[something].getAllergien().length; i++){
                if(i != 99){
                    patienten[something].getAllergien()[i] = patienten[something].getAllergien()[i + 1];
                }
            }
            jListAllerg.setSelectedIndex(-1);
            ValueChangedInJList1();
        }
        Speichern();
    }//GEN-LAST:event_jListAllergMouseClicked

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if(evt.getButton() == MouseEvent.BUTTON3){
            int something = jList1.getSelectedIndex();
            jList1.setSelectedIndex(-1);
            patienten[something] = null;
            for(int i = something; i < patienten.length; i++){
                if((i+1 != patienten.length))
                patienten[i] = patienten[i+1];
                else
                    patienten[i] = null;
            }
            Update();
            jList1.setSelectedIndex(-1);
            ValueChangedInJList1();
            Speichern();
        }
    }//GEN-LAST:event_jList1MouseClicked

